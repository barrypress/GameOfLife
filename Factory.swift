    /// Generates random cells of size/size
    static func random(size: Int) -> [[Int]]
    {
        var cells = Array(repeating: Array(repeating: 0, count: size), count: size)
        let odds = 1.0 / 5.0
        
        for x in 0..<size {
            for y in 0..<size {
                if Double.random(in: 0..<1) < odds {
                    cells[x][y] = 1
                }
            }
        }
        
